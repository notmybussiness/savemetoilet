// 긴급도에 따른 아이콘 색상
export const getUrgencyIcon = (urgency, className = "h-6 w-6") => {
  const baseClasses = className;
  
  switch (urgency) {
    case 'emergency':
    case 'high':
      return <IconToilet className={`${baseClasses} text-red-500`} />;
    case 'moderate':
    case 'medium':
      return <IconToilet className={`${baseClasses} text-yellow-500`} />;
    case 'relaxed':
    case 'low':
      return <IconToilet className={`${baseClasses} text-green-500`} />;
    default:
      return <IconToilet className={`${baseClasses} text-gray-500`} />;
  }
};